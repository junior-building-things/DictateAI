const translationAlias: Record<LanguageCode, keyof typeof translations> = {
  en: "en",
  zh: "zh",
  sv: "sv",
  fi: "fi",
};

const I18nContext = createContext<I18nContextValue | null>(null);

function interpolate(input: string, vars?: Record<string, string | number>): string {
  if (!vars) return input;
  return input.replace(/\{\{(.*?)\}\}/g, (_, k: string) => String(vars[k.trim()] ?? ""));
}

export function translateForLanguage(
  language: LanguageCode,
  key: TranslationKey,
  vars?: Record<string, string | number>,
) {
  const table = (translations[translationAlias[language]] ?? translations.en) as Partial<
    Record<TranslationKey, string>
  >;
  const text = table[key] ?? translations.en[key];
  return interpolate(text, vars);
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>("en");

  useEffect(() => {
    getSetting("interface_language")
      .then((value) => {
        if (value in translationAlias) {
          setLanguageState(value as LanguageCode);
        }
      })
      .catch(() => {
        setLanguageState("en");
      });
  }, []);

  const setLanguage = async (next: LanguageCode) => {
    setLanguageState(next);
    await saveSetting("interface_language", next);
  };

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: TranslationKey, vars?: Record<string, string | number>) => {
      return translateForLanguage(language, key, vars);
    };

    return { language, setLanguage, t };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return ctx;
}

